    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {

        UserInterface ui;

        String username = request.getParameter("username");
        //String lastname = request.getParameter("lastName");
        String emailaddress = request.getParameter("inputEmail");
        String password = request.getParameter("inputPassword");

        RegisterDao regd = new RegisterDao();

        //RegisterDao 1d = new RegisterDao();
        if (!regd.Check(username, emailaddress)) {
            ui = new UsersRepository();

            if (!username.trim().isEmpty() && !password.trim().isEmpty() && !emailaddress.trim().isEmpty()) {
                Users user = new Users();
                user.setName(username);
                user.setPassword(password);
                user.setEmail(emailaddress);
                UserRoles ur = new UserRoles();
                ur.setRoleId(1);
                user.setRoleId(ur);
                user.setUserId(9999);

                ui.addUser(user);
            }

            response.sendRedirect("login.jsp");
        } else if (!regd.Check(username, password)) {
            request.getSession().setAttribute("errorMessasge", "Username or Email already exists!");
            RequestDispatcher rd = request.getRequestDispatcher("register.jsp");
            rd.forward(request, response);
        }

    }

